# if / elif       / else
# se / se não se  / se não

condicao1 = False
condicao2 = False
condicao3 = True
condicao4 = False


# If e Elif só executa a condição que for verdadeira
if condicao1:
    print('Este é o código da condição 1')
elif condicao2:
    print('Este é o código da condição 2')
elif condicao3:
    print('Este é o código da condição 3')
elif condicao4:
    print('Este é o código da condição 4')
else:
    print('Nenhuma condição foi feita')

if 10 == 10:
    print('Segundo if')

print('Fora do if')