print(1234)

# Aspas simples -> ''
print('Patrick Marques')
print(1, 'Patrick "Marques"')
 
# Aspas duplas -> ""
print("Patrick Marques")
print(2, "Patrick 'Marques'")

# Quase nunca usados
# Escape -> \
print("Patrick \"Marques\"")

# r -> exibe o Escape
print(r"Patrick \"Marques\"")